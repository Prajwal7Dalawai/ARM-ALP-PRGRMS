	AREA evenodd,CODE,READONLY
		LDR R0,=ARR
		MOV R1,#05	;NO. OF ELEMENTS IN THE ARRAY
		MOV R2,#00	;COUNT OF EVEN NUMBERS
		MOV R3,#00	;COUNT OF ODD NUMBERS
NEXTNUM	LDR R4,[R0],#04
		MOV R4,R4,LSR #01
		BHI ODDNO
		ADD R2,R2,#01
		B NEXT
ODDNO	ADD R3,R3,#01
NEXT	SUBS R1,R1,#01
		CMP R1,#0
		BNE NEXTNUM
		NOP
ARR DCD 0X1, 0X2, 0X3, 0X4, 0X5
	END